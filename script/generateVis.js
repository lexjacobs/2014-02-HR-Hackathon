var datasetLoader = function(selectedDay){
  result = [];
  for (var i = 0; i < tripData.length; i++) {
    if(tripData[i][4] === selectedDay){  
      result.push(tripData[i]);
    }
  }
  // console.log('dataset length: ', result.length);
  return result;
};

